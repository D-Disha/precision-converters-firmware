/* @brief	Main function
 * @details	This is a main entry function for AD2S1210 IIO application
 */
int main(void)
{
	/* Initialize the AD2S1210 IIO interface */
	if (ad2s1210_iio_initialize()) {
		printf("IIO initialization failure!!\r\n");
	}

	while (1) {
		ad2s1210_iio_event_handler();
	}

}
